                               <p><i>Mimoza Studenica and Andjela Jordanovic, live and work in Kosovo; one in Pristina, the other in Mitrovica. Canvas, paper, paints – are their common "tool". They create works of art using different techniques, however, they treat artistic creation with equal love.</i></p><p>They share the same education - both graduated in graphic design, at the Faculty of Arts, University of Pristina, or Mitrovica. While twenty-three-year-old Andjela is engaged in graphics, painting and drawing and does intaglio printing, ie aquatint and etching, forty-six-year-old Mimoza uses the technique of filigree paper on canvas.</p><p>The universal motif that permeates the work of Mimoza Studenica is the motif of a woman, so among her works you can see Marilyn Monroe, Kate Moss, Geisha, Japanese woman...</p><p>"I feel that a woman is the basis of a society and that is why I chose to represent her through my work," she points out.</p><p>On the other hand, Andjela, as she states, is engaged in researching motives:</p><p>"I explore figures, portraits, space, lighting, shadows..."</p><p>Everyday life - inspires both of them to create.</p><p>"I find inspiration every day, in the people around me, in some of my daily research on art," says Andjela, a master's student.</p><p>The ordinary viewer notices the details of everyday life in Andjela's works.</p><p><i>Art is an integral part of their lives, and what is their first thought when someone says ‘art’?</i></p><p>"Life," Mimosa replies, "expression," adds Andjela.</p><p>"Art is a creation that we leave behind, whether in written or cartoon form, or works that we undertake during our lifetime," explains Mimoza, who works for Radio Television of Kosovo as a graphic designer for promotion.</p><p>Andjela approaches the concept of art in a slightly different way.</p><p>"Art is, above all, education, knowledge of the history of art, as well as the theory of form, so that through the acquired critical attitude towards art, we can create or understand by ourselves," she explains.</p><p>Art is also creativity that we leave behind and education, knowledge of history and theory, but it is also a connection that connects individuals and communities and which in its own way contributes to building trust.</p><p><i>How do artists see trust?</i></p><p>"Trust is faith in other human being," Mimoza said.</p><p>"Trust is without a doubt, security for me," concludes Andjela.</p><div><i>Using this text is allowed with a clear indication of the source and linking to the source of the text or the home page of the platform. &nbsp;</i><br></div><p></p>
