                    <div class="entry-portfolio-content col-md-8 text-justify">
  <p>Distanca fizike nuk ka nevojë të thotë izolim. Platforma
Kosovare e Ndërtimit të Besimit mund të ndihmojë në mënyrë digjitale të
lehtësojë barrën duke ju sjellë histori pozitive të njerëzve që punojnë së
bashku nëpër komunitete, frymëzim nga kampionët që promovojnë besim në mënyra
të reja dhe inovative, si dhe aftësinë për t'u lidhur dhe ushtruar solidaritet
me të tjerët në një kohë kur çdo ditë e zakonshme rutinat dhe lidhjet shoqërore
janë ndërprerë.<br>
<br>
Për shkak të sfidave të paraqitura nga pandemia aktuale COVID-19, ndërtimi i
besimit dhe Platforma kanë marrë një rëndësi të re. Besimi tek njëri-tjetri nuk
ka qenë kurrë më qenësor për mirëqenien tonë kolektive. Qëllimi i kësaj faqe
është të mbledhë në një vend lidhjet e rëndësishme për informacionin mbi
COVID-19, si dhe në lidhjet e vetë-ndihmës gjatë vetë-izolimit.<br>
<br>
<u>Informohuni për COVID-19 nga burime zyrtare:<br>
</u><br>
&nbsp;- <a target="_blank" rel="nofollow" href="https://www.who.int/"><b>Organizata Botërore e Shëndetësisë</b></a><br>
&nbsp;- <a target="_blank" rel="nofollow" href="https://api.whatsapp.com/send?phone=41797818791&amp;text=hi&amp;source&amp;data&amp;lang=sq"><b>Aleanca e Shëndetit e OBSH-së</b></a><br>
&nbsp;- <a target="_blank" rel="nofollow" href="https://www.kosova.health/"><b>Faqja e raportimit COVID-19 e Kosovës</b></a><br>
&nbsp;- <a target="_blank" rel="nofollow" href="https://msh.rks-gov.net/"><b>Ministria e Shëndetësisë e Kosovës</b></a><br>
<br>
<u>Shikoni përditësimet më të fundit mbi COVID-19 në platformën tonë:</u><br>
<br>
Organizata Botërore e Shëndetit ka prodhuar një <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/266"><b>video</b></a>&nbsp;në COVID-19 dhe si të mbrohemi.<br>
<br>
Përfaqësuesi Special i Sekretarit të Përgjithshëm për Kosovën, Zahir Tanin, <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/306">u bën thirrje
udhëheqësve që të tregojnë unitet në luftën kundër pandemisë</a>, dhe ai
gjithashtu <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/349">bën
thirrje për respektimin e të drejtave të njeriut në Kosovë</a> në kontekstin e
COVID-19.</p>

<p>Ambasadorja Natalia Apostolova, Shefe e Zyrës së BE-së në
Kosovë dhe Përfaqësues Special i BE-së, këshillon që pandemitë të <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/350">lihen mënjanë nga
debati politik dhe debati i padobishëm</a>. Ndërsa Avokati i Popullit u kërkoi <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/351">mediave të mos
publikojnë emrat e personave të karantinuar</a>, në mënyrë që të mbrojnë të
drejtat e personave të infektuar nga virusi.</p>

<p>Fondi i Popullsisë i Kombeve të Bashkuara për Gratë dhe
Kombeve të Bashkuara (UNFPA) ka lëshuar rekomandime të ndjeshme ndaj gjinive
mbi vështirësitë në zhvillim me të cilat përballen gratë dhe vajzat gjatë
pandemisë COVID-19. Shihni në: <a target="_blank" rel="nofollow" href="https://bit.ly/3axroyg"><b>https://bit.ly/3axroyg</b></a><br>
&nbsp;<br>
Gjithashtu, Grupi për Siguri dhe Barazi Gjinore bën thirrje për parandalimin e
dhunës në familje në mes të pandemisë COVID-19. Lexoni më shumë në: <a target="_blank" rel="nofollow" href="https://bit.ly/2UlEedD"><b>https://bit.ly/2UlEedD</b></a><br>
<br>
Partnerët tanë kanë qenë gjithashtu aktivë në ngritjen e vetëdijes për
pandeminë COVID-19. Në videon e tyre, KFOR-i po dërgon një mesazh të fuqishëm
se <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/283"><b>Duke mbrojtur veten tonë, ne
mbrojmë të tjerët</b></a>. Platforma e mësimit të gjuhës VocUp po na
tregon se <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/266"><b>Jo të gjitha fjalët janë po aq
të rëndësishme për çdo kohë!</b></a>&nbsp;dhe se disa nga fjalët më të
rëndësishme këto ditë janë: Qëndroni në shtëpi. #stayathome #rrinështëpi
#ostanikodkuće</p>

<p><a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/333">UNKT,
UNFPA Kosova dhe UNDP Kosova</a> kanë dorëzuar mbi 8,500 artikuj ushqimorë
familjeve në rrezik. <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/341">UNKT, UN Women Kosova
dhe UNICEF Kosova</a> kanë dhuruar ushqime, pajisje higjienike dhe mallra të
tjera në shtëpi të sigurta në Kosovë. Ndërsa <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/343">UNICEF i dha
mbështetje të jashtëzakonshme Ministrisë së Shëndetësisë</a> në Kosovë me
dhurimin e pajisjeve mjekësore.</p>

<p><a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/318">Qendra
e Inovacionit në Mitrovicë</a> dhe <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/312">Qendra e Inovacionit
Graçanica</a> kanë krijuar dhe dhuruar mbrojtëse për fytyrë për profesionistët
mjekësorë. Ata gjithashtu inkurajojnë këdo që posedon printerë 3D të bëjnë të
njëjtën gjë.</p>

<p>Rrjeti i Aktivizmit Qytetar, në bashkëpunim me Zyrën e
Komisionerit për Gjuhët, fton të gjithë përkthyesit dhe lektorët në gjuhën
serbe, si dhe gjuhët e tjera jo-shumicë në Kosovë, të bashkohen me iniciativën
tonë vullnetare <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/315">COVID
19 - të përgjegjshëm për të gjithë</a>. Ndërsa Voc Up gjithashtu <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/326">ofron ndihmë falas
për institucionet shëndetësore në Kosovë</a> për përkthimin dhe redaktimin në
gjuhët zyrtare të atyre dokumenteve që synojnë të informojnë publikun për masat
kundër sëmundjes infektive.</p>

<p><br>
<u>Këtu është gjithashtu një listë e lidhjeve të dobishme se si të kaloni kohë
cilësore në izolim:</u><br>
<br>
Qëndrimi në shtëpi mund të jetë një mundësi e shkëlqyeshme për të mësuar gjuhë
dhe aftësi të reja! Shfrytëzojeni këtë mundësi për të mësuar serbisht / shqip
përmes <a target="_blank" rel="nofollow" href="http://www.voc-up.com/"><b>www.voc-up.com</b></a>.
Ju gjithashtu mund të shkarkoni aplikacione celularë në Google Play dhe Apple
Store.</p>

<p>Barazia gjinore fillon me ju! Stafi i <a target="_blank" rel="nofollow" href="https://medium.com/@UN_Women/gender-equality-starts-with-you-nine-ways-you-can-keep-up-your-activism-from-your-couch-624624c5fabe">Kombeve
të Bashkuara për Gratë</a> ka hartuar nëntë mënyra se si të mbroni barazinë
gjinore nga dhoma juaj e jetesës. Gjëja më e rëndësishme është të qëndroni në
shtëpi dhe të sigurt, dhe <a target="_blank" rel="nofollow" href="https://www.facebook.com/pg/unwomenkosovo/photos/?tab=album&amp;album_id=822278404958364&amp;ref=page_internal">UN
Women Kosova</a> ka një album të veçantë në faqen e saj në Facebook, ku mund të
gjeni ide të ndryshme se si ta kaloni cilësinë e kohës së lirë.</p>

<p><br>
Ju gjithashtu mund ta përdorni këtë kohë për t'u frymëzuar! Nëse ndiheni sikur
jeni gati për të ndryshuar botën, konsideroni të aplikoni për <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/294"><b>Bursën e Korpusit të Atlasit</b></a>. Ju
gjithashtu mund të shikoni listën e librave tanë të <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sq/stories/295"><b>preferuar feministe</b></a>&nbsp;nëse jeni jashtë
ideve çfarë të lexoni.</p>

<p>Izolimi dhe koha e lirë na japin shumë kohë për të menduar,
dhe ndonjëherë mund të na shqetësojnë. Relaksohuni dhe <a target="_blank" rel="nofollow" href="https://soundcloud.com/untangle">meditoni</a> në “<a target="_blank" rel="nofollow" href="https://www.youtube.com/user/yogawithadriene/videos">Yoga with Adriene</a>”!
Nëse gjeni kënaqësi në gatim, <a target="_blank" rel="nofollow" href="https://www.image.ie/life/cabin-fever-cooking-chefs-showing-cook-online-covid-19-186516">gatuajini
me kuzhinierët</a> më të mirë në internet! Dhe në lidhjen e mëposhtme mund të
lexoni disa histori pozitive rreth virusit të koronës për t'ju ndihmuar të
merrni frymë më lehtë këto ditë: <a target="_blank" rel="nofollow" href="https://bit.ly/2Xj9ig0">https://bit.ly/2Xj9ig0</a></p>

<p>Nëse i keni përfunduar librat nga biblioteka juaj private
ose ju mungon një libër i preferuar në koleksionin tuaj, tani mund t'i lexoni
në internet! <a target="_blank" rel="nofollow" href="https://www.instagram.com/p/B94KiBzg4iq/"><b>Scribd</b></a>&nbsp;ka
hapur bibliotekën e tyrepër muajin tjetër, ndërsa <a target="_blank" rel="nofollow" href="https://www.instagram.com/p/B9rcPmKJEZF/"><b>Bookmate</b></a>&nbsp;po ofron deri në 30 ditë
lexim falas për përdoruesit e saj të rinj në Mars. Të gjithë librat nga
Biblioteka e <a target="_blank" rel="nofollow" href="https://images.nasa.gov/"><b>NASA</b></a>&nbsp;tani mund të lexohen në
internet!</p>

<p>A e dini se çfarë është një <a target="_blank" rel="nofollow" href="https://www.youtube.com/user/crashcourse/about">Crash course</a>? Një
gamë e kurseve fenomenale në një kanal. Mësoni fakte interesante nga
sociologjia, psikologjia, historia, mitologjia dhe shumë shkenca të tjera.
Njohuria nuk është kurrë e mjaftueshme dhe koha po mbaron!</p>

<p>Tani mund të udhëtoni nëpër botë nga kolltuku juaj. <a target="_blank" rel="nofollow" href="https://artsandculture.google.com/partner?hl=en">Google Guide</a> ju merr
nëpër muzetë më të famshëm në botë me vetëm një klik. Gjithashtu, vizitoni
ekspozitën "<a target="_blank" rel="nofollow" href="https://www.lapsi360.com/DCK/">Një herë një herë, jo
përsëri</a>" të Qendrës së Ligjit Humanitar të Kosovës.</p>

<p>A keni kitarë? Fantastike! Mësoni këngët e artistëve tuaj të
preferuar. Mund <a target="_blank" rel="nofollow" href="https://www.guitartricks.com/index.php?a_aid=54ad763594aab&amp;campaignid=6498732654&amp;adgroupid=88295620462&amp;adid=413256702926&amp;gclid=Cj0KCQjwybD0BRDyARIsACyS8mudLNRT7VoU6to6GFXnqAn73r-XTDLbKjSCh2u7EG_BBtShTkZd1ysaAs6WEALw_wcB#form-signup">të
gjeni</a> mbi 1000 këngë të reja në javë në një vend. E gjithë kjo me një
llogari falas.</p>

