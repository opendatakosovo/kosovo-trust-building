                    <div class="row">
                        
                    <div class="entry-portfolio-content col-md-8 text-justify">
  <p></p>
  
                    <p>Physical distancing does not need to mean isolation. The
                    Kosovo Trusbuilding Platform can help digitally ease the burden by bringing you
                    positive stories of people working together across communities, inspiration
                    from champions forging trust in new and innovative ways, and the ability to
                    connect and exercise solidarity with others at a time when our normal daily
                    routines and social ties have been disrupted.<br>
                    <br>
                    Because of the challenges posed by the current COVID-19 pandemic, trustbuilding
                    and the Kosovo Trustbuilding Platform have taken on new importance. Trust in
                    one another has never been more critical to our collective well-being. The
                    purpose of this page is to gather in one place the important links to
                    information on COVID-19, as well as on the self-help links during
                    self-isolation.<br>
                    <br>
                    <u>Get informed on COVID-19 from official sources:</u><br>
                    &nbsp;<br>
                    - <a target="_blank" rel="nofollow" href="https://www.who.int/"><b>World Health Organization</b></a><br>
                    - <a target="_blank" rel="nofollow" href="https://api.whatsapp.com/send?phone=41797818791&amp;text=hi&amp;source=&amp;data"><b>WHO Health Alert</b></a><br>
                    - <a target="_blank" rel="nofollow" href="https://www.kosova.health/en/"><b>Kosovo COVID-19 Reporting Page</b></a><br>
                    - <a target="_blank" rel="nofollow" href="https://msh.rks-gov.net/en/"><b>Kosovo Ministry of Health</b></a><br>
                    <br>
                    <br>
                    <u>Check out latest updates on COVID-19 on our platform</u>:<br>
                    <br>
                    World Health Organization has produced a <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/266"><b>video</b></a>&nbsp;on COVID-19 and how to protect
                    ourselves.<br>
                    <br>
                    Special Representative of the Secretary-General for Kosovo, Zahir Tanin, <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/306">calls upon leaders to
                    demonstrate unity combating COVID-19</a>. He also <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/349">calls for upholding
                    human rights in Kosovo</a> in the context of COVID-19.</p>

                    <p>Head of the EU Office in Kosovo and EU Special
                    Representative, ambassador Nataliya Apostolova, calls for <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/350">leaving political
                    debates and pointless debates aside</a> during the pandemic. Furthermore, the Ombudsman
                    asked <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/351">the media
                    not to publish the names of quarantined persons</a> in order to protect the
                    rights of people who have been infected with the virus.<br>
                    <br>
                    UN Women and UNFPA have issued Gender-Sensitive Recommendations on emerging
                    issues for Women and Girls during the COVID-19 pandemic. Check it out at: <a target="_blank" rel="nofollow" href="https://bit.ly/33KIC92"><b>https://bit.ly/33KIC92</b></a><br>
                    &nbsp;<br>
                    Also, Security and Gender Group has called for the Prevention of Domestic
                    Violence amid the COVID-19 pandemic. Read more at the following link: <a target="_blank" rel="nofollow" href="https://bit.ly/2wt8kTr"><b>https://bit.ly/2wt8kTr</b></a><br>
                    <br>
                    Our trustbuilding partners have also been active in raising awareness on
                    COVID-19 pandemic. In their video, KFOR is sending a powerful message that
                    by <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/283"><b>Protecting Ourselves, we Protect
                    Others.</b></a>&nbsp;The VocUp Language Learning Platform is showing us
                    that <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/281"><b>Not all Words are Equally
                    Important at all Times!</b></a>&nbsp;and that some of the most important
                    words these days are: Stay at home. #stayathome #rrinështëpi #ostanikodkuće</p>

                    <p><a target="_blank" rel="nofollow"></a><a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/333">UNKT, UNFPA
                    Kosovo and UNDP Kosovo</a> delivered over 8,500 food items to vulnerable families. <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/341">UNKT, UN Women
                    Kosovo and UNICEF Kosovo</a> donated food,
                    hygiene kits and the other type of supplies to safe houses in Kosovo. <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/343">UNICEF also
                    provided tremendous support to the
                    Ministry of Health</a> in Kosovo by
                    donating medical equipment.</p>

                    <p><a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/318">Mitrovica
                    Innovation Center</a> and <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/312">Gracanica Innovation Center</a> produced and
                    donated protective visors for healthcare workers. They have also called on those
                    who have 3D printers to do the same.</p>

                    <p>In collaboration with the Office of Language Commissioner,
                    Network for Civic Activism invites all Serbian language translators and proofreaders,
                    but also translators and proofreaders of other non-majority languages in Kosovo to
                    join our volunteer initiative/action <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/315">COVID-19 – responsible for everybody</a>. VocUp also <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/326">offers help to health institutions in Kosovo</a> by providing
                    free translation and proofreading of the documents (intended to inform citizens
                    on measures against infectious disease) into the official languages.<br>
                    &nbsp;<br>
                    <u>Here is also a list of the useful links on how to spend quality time in
                    self-isolation:</u>&nbsp;<br>
                    &nbsp;<br>
                    Staying at home can be a great opportunity to learn new languages and skills!
                    Take this opportunity to learn Serbian/Albanian through <a target="_blank" rel="nofollow" href="http://www.voc-up.com/"><b>www.voc-up.com</b></a>&nbsp;.
                    You can also download mobile apps on Google Play and Apple Store.</p>

                    <p>Gender equality starts with you! The <a target="_blank" rel="nofollow" href="https://medium.com/@UN_Women/gender-equality-starts-with-you-nine-ways-you-can-keep-up-your-activism-from-your-couch-624624c5fabe">UN Women</a> staff has come up with nine ways to
                    support gender equality without leaving your living rooms. The most important
                    thing is to stay at home and stay safe, and the <a target="_blank" rel="nofollow" href="https://www.facebook.com/pg/unwomenkosovo/photos/?tab=album&amp;album_id=822278404958364&amp;ref=page_internal">UN Women Kosovo</a> has a special album on its Facebook
                    page, where you can find various ideas on spending quality free time.<br>
                    <br>
                    You can also use this time to get inspired! If you feel like you are ready to
                    change the world, consider applying for the <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/294"><b>Atlas Corps Fellowship</b></a>. You can also check
                    out the list of our favorite <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/en/stories/295"><b>feminist books </b></a>if you are out of
                    ideas about what to read.</p>

                    <p>Isolation and free time have given us a lot of time to think,
                    and sometimes it can weigh us down. Yoga classes with “<a target="_blank" rel="nofollow" href="https://www.youtube.com/user/yogawithadriene/videos">Yoga with Adriene</a>” can help you relax and <a target="_blank" rel="nofollow" href="https://soundcloud.com/untangle">meditate</a>. If you enjoy
                    cooking, you can cook with the world’s best chefs online! By clicking the
                    following link, you can also read a couple of positive stories about coronavirus
                    that can help you become less tense and less anxious these days: <a target="_blank" rel="nofollow" href="https://bit.ly/2Xj9ig0">https://bit.ly/2Xj9ig0</a> <br>
                    <br>
                    If you are about to finish reading your books from your private library or if
                    you don't have your favorite book in your collection, you can now read
                    online! <a target="_blank" rel="nofollow" href="https://www.instagram.com/p/B94KiBzg4iq/"><b>Scribd</b></a>&nbsp;has opened up access to its
                    digital library for the next month and, during March, <a target="_blank" rel="nofollow" href="https://www.instagram.com/p/B9rcPmKJEZF/"><b>Bookmate</b></a>&nbsp;has offered a free 30-day
                    access to reading books to its new customers. You can read all of the books
                    from the <a target="_blank" rel="nofollow" href="https://images.nasa.gov/"><b>NASA</b></a>&nbsp;Library online!</p>

                    <p>Have you ever heard about <a target="_blank" rel="nofollow" href="https://www.youtube.com/user/crashcourse/about">Crash course</a>? There
                    are tons of phenomenal courses on one channel. Learn some interesting facts
                    related to sociology, psychology, history, mythology and many other sciences.
                    Knowledge is never enough and we've got plenty of time!</p>

                    <p>You can now travel the world without leaving your armchair. <a target="_blank" rel="nofollow" href="https://artsandculture.google.com/partner?hl=en">Google Guide</a> takes
                    you through the most famous museums in the world, with just a click. You can
                    also visit the exhibition “<a target="_blank" rel="nofollow" href="https://www.lapsi360.com/DCK/">Once upon a
                    time and never again</a>”, organized by the Humanitarian Law Center in Kosovo.</p>

                    <p>Do you have a guitar? Fantastic! Learn how to play songs of
                    your favorite artists. You can find over 1000 new songs a week, in <a target="_blank" rel="nofollow" href="https://www.guitartricks.com/index.php?a_aid=54ad763594aab&amp;campaignid=6498732654&amp;adgroupid=88295620462&amp;adid=413256702926&amp;gclid=Cj0KCQjwybD0BRDyARIsACyS8mudLNRT7VoU6to6GFXnqAn73r-XTDLbKjSCh2u7EG_BBtShTkZd1ysaAs6WEALw_wcB#form-signup">one place</a>. You can do it by creating a free
                    account.</p>

                    <p>Have fun with your kids and siblings. Here are some tips to
                    keep them focused for more than 5 minutes: <a target="_blank" rel="nofollow" href="https://bit.ly/34i7MvU">https://bit.ly/34i7MvU</a> </p>

                    <p>If you are feeling lonely, check out this podcast on how to&nbsp;<a target="_blank" rel="nofollow" href="https://castbox.fm/episode/id2221151-id240696768?utm_campaign=android_old_share&amp;utm_medium=dlink&amp;utm_source=android_old_share&amp;country=us"><b>Beat Your
                    Isolation Loneliness</b></a><b><u>.</u></b><br>
                    &nbsp;</p><p>Stay
                    home and stay safe!</p><br><p></p>

