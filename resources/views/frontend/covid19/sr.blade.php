                    <div class="entry-portfolio-content col-md-8 text-justify">
  <p>Fizička
razdvojenost ne mora nužno značiti isto što i izolacija. Platforma za izgradnju
poverenja na Kosovu može nam, posredstvom digitalnih tehnologija, olakšati
teret pozitivnim pričama o ljudima koji rade zajedno u zajednicama,
inspiracijom koju pružaju šampioni koji poverenje grade na nove i inovativne
načine, kao i sposobnošću povezivanja i vežbanja solidarnosti sa drugima u
vreme kada su naše uobičajene, svakodnevne rutine i društvene veze otežane.</p>

<p>Upravo
zbog izazova koji pandemija COVID-19 predstavlja, izgradnja poverenja i
Platforma za izgradnju poverenja na Kosovu poprimili su novi značaj. Međusobno
poverenje nikada nije bilo od tolikog značaja za kolektivno blagostanje svih
nas. Svrha ove stranice je da na jednom mestu sakupi sve važne linkove sa
informacijama o pandemiji COVID-19, kao i linkove o tome kako možemo pomoći
sebi u periodu samoizolacije.<br>
<br>
&nbsp;<u>Informišite se o pandemiji COVID-19 putem zvaničnih izvora:</u><br>
<br>
- <a target="_blank" rel="nofollow" href="https://www.who.int/"><b>Svetska zdravstvena organizacija</b></a><br>
- <a target="_blank" rel="nofollow" href="https://api.whatsapp.com/send?phone=41797818791&amp;text=hi&amp;source&amp;data&amp;lang=sr"><b>SZO
Zdravstveno upozorenje</b></a><br>
- <a target="_blank" rel="nofollow" href="https://www.kosova.health/sr/"><b>Kosovo COVID-19 stranica
izveštaja</b></a><br>
- <a target="_blank" rel="nofollow" href="https://msh.rks-gov.net/sr/"><b>Ministarstvo zdravlja Kosova</b></a><br>
<br>
&nbsp;<u>Pogledajte najnovije informacije o pandemiji COVID-19 na našoj
Platformi:</u><br>
<br>
Svetska zdravstvena organizacija pripremila je <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/266"><b>video</b></a>&nbsp;o pandemiji COVID-19 i o
tome kako da se zaštitimo.<br>
<br>
Specijalni predstavnik Generalnog sekretara za
Kosovo, Zahir Tanin, <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/306">poziva lidere da pokažu jedinstvo u borbi protiv pandemije</a> , takođe on <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/349">poziva na poštovanje ljudskih prava na Kosovu</a> u kontekstu COVID-19.</p>

<p>Ambasadorka Natalija Apostolova,
šefica Kancelarije EU na Kosovu i specijalna predstavnica EU savetuje da se u
ovoj situaciji pandemije <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/350">ostave po strani političke rasprave i beskorisne rasprave</a>. Dok je Ombudsman zatražio od <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/351">medija da ne objavljuju imena lica koja se nalaze u karantinu</a>, kako bi zaštitili prava ljudi koji su zaraženi virusom.</p>

<p>Agencija
UN Žene i Fond za stanovništvo Ujedinjenih nacija (UNFPA) izdale su rodno
senzitivne preporuke o novonastalim poteškoćama s kojima se žene i devojke
suočavaju tokom pandemije COVID-19. Pogledajte na: <a target="_blank" rel="nofollow" href="https://bit.ly/2vQGOPj"><b>https://bit.ly/2vQGOPj</b></a><br>
<br>
Takođe, Grupa za sigurnost i rodnu ravnopravnost poziva na sprečavanje nasilja
u porodici usred pandemije COVID-19. Više o tome pročitajte na: <a target="_blank" rel="nofollow" href="https://bit.ly/3dqhJM9"><b>https://bit.ly/3dqhJM9</b></a><br>
<br>
Naši partneri za izgradnju poverenja takođe su bili aktivni u podizanju svesti
o pandemiji COVID-19. U svom videu, KFOR šalje snažnu poruku da time što <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/283"><b>Štiteći sebe, štitimo druge.</b></a>&nbsp;Platforma za učenje jezika
,,VocUp” pokazuje nam da<a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/281"><b>&nbsp;Nisu sve reči podjednako
važne</b></a>&nbsp;u
svakom trenutku i da su neke od najvažnijih reči ovih dana: Ostanite kod kuće.
#stayathome #rrinështëpi #ostanikodkuće</p>

<p><a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/333">UNKT,
UNFPA Kosovo i UNDP Kosovo</a> uručili su preko 8.500 prehrambenih artikala
ugroženim porodicama. <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/341">UNKT, UN Women Kosovo
i UNICEF Kosovo</a> su donirali hranu, higijenske komplete i drugu robu
sigurnim kućama na Kosovu. Dok je <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/343">UNICEF pružio ogromnu
podršku Ministarstvu zdravlja</a> na Kosovu uz doniranje medicinske opreme. </p>

<p><a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/318">Inovacioni
centar Mitrovica</a> i <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/312">Inovacioni centar
Gračanica</a> napravili su i donirali zaštitne vizire za medicinske radnike.
Takođe, pozivaju sve koji poseduju 3D štampače da učine isto.</p>

<p>Mreža za građanski aktivizam u saradnji sa Kancelarijom poverenika za jezike, pozivaju sve
prevodioce i lektore za srpski, ali i druge nevećinske jezike na Kosovu, da se
priključe našoj volonterskoj inicijativi/akciji <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/315">COVID 19 - odgovorni prema svima</a>. Dok Voc Up, takođe, <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/326">nudi besplatnu pomoć zdravstvenim ustanovama na Kosovu</a> za prevode i lekturisanje na
službene jezike onih dokumenata koji su namenjeni masovnom informisanju građana
o merama protiv zarazne bolesti.<br>
<br>
<u>Evo i liste korisnih linkova sa informacijama o tome kako kvalitetno
provesti vreme u samoizolaciji:</u><br>
<br>
Boravak kod kuće može biti odlična prilika za učenje novih jezika i veština!
Iskoristite ovu priliku da naučite srpski/albanski jezik na <a target="_blank" rel="nofollow" href="http://www.voc-up.com/"><b>www.voc-up.com</b></a>. Takođe, možete da preuzmete
aplikacije za mobilne telefone putem internet prodavnica Google Play i Apple
Store.</p>

<p>Rodna ravnopravnost počinje baš od vas! Zaposleni u <a target="_blank" rel="nofollow" href="https://medium.com/@UN_Women/gender-equality-starts-with-you-nine-ways-you-can-keep-up-your-activism-from-your-couch-624624c5fabe">UN
Women</a> su osmislili devet načina kako možete zagovarati rodnu ravnopravnost
iz vaše dnevne sobe. Najbitnije je ostati u kući i na sigurnom, a <a target="_blank" rel="nofollow" href="https://www.facebook.com/pg/unwomenkosovo/photos/?tab=album&amp;album_id=822278404958364&amp;ref=page_internal">UN
Women Kosovo</a> ima poseban album na svojoj Facebook stranici, gde možete
pronaći razne ideje kako kvalitetno provesti svoje slobodno vreme.</p>

<p>Ovo
vreme možete iskoristiti i tako što ćete pronaći ono što vas inspiriše i
pokrenuti se! Ako se osećate kao da ste spremni da promenite svet, razmislite o
konkurisanju za <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/294"><b>Atlas Corps stipendije</b></a>. Takođe, možete pogledati listu
naših omiljenih <a target="_blank" rel="nofollow" href="https://www.kosovotrustbuilding.com/sr/stories/295"><b>feminističkih knjiga</b></a>, ako nemate ideju o
tome šta biste mogli da čitate.</p>

<p>Izolacija i slobodno vreme nam pružaju dosta vremena za
razmišljanje, a nekad to zna da nas optereti. Opustite se i <a target="_blank" rel="nofollow" href="https://soundcloud.com/untangle">meditirajte</a> uz časove joge “<a target="_blank" rel="nofollow" href="https://www.youtube.com/user/yogawithadriene/videos">Yoga with Adriene</a>”!
Ako vam kuvanje pričinjava zadovoljstvo, kuvajte sa najboljim <a target="_blank" rel="nofollow" href="https://www.image.ie/life/cabin-fever-cooking-chefs-showing-cook-online-covid-19-186516">svetskim
kuvarima</a> onlajn! A na sledećem linku možete pročitati par pozitivnih priča
o korona virusu, kako biste lakše disali ovih dana: <a target="_blank" rel="nofollow" href="https://bit.ly/2Xj9ig0">https://bit.ly/2Xj9ig0</a> <br>
<br>
Ukoliko knjige iz privatne biblioteke privodite kraju ili vam omiljena knjiga
nedostaje u kolekciji, sada možete čitati i onlajn! <a target="_blank" rel="nofollow" href="https://www.instagram.com/p/B94KiBzg4iq/"><b>Scribd</b></a>&nbsp;je otvorio svoju
biblioteku na narednih mesec dana, dok <a target="_blank" rel="nofollow" href="https://www.instagram.com/p/B9rcPmKJEZF/"><b>Bookmate</b></a>&nbsp;tokom marta meseca svojim
novim korisnicima nudi čak 30 dana besplatnog čitanja. Sve knjige iz <a target="_blank" rel="nofollow" href="https://images.nasa.gov/"><b>NASA</b></a>&nbsp;biblioteke
sada možete pročitati onlajn!</p>

<p>Znate li šta je <a target="_blank" rel="nofollow" href="https://www.youtube.com/user/crashcourse/about">Crash course</a>? Tona
fenomenalnih kurseva na jednom kanalu. Naučite zanimljive činjenice iz
sociologije, psihologije, istorije, mitologije i mnogih drugih nauka. Znanja
nikad dosta, a vremena je na pretek! </p>

<p>Sada
možete obići svet iz svoje fotelje. <a target="_blank" rel="nofollow" href="https://artsandculture.google.com/partner?hl=en">Google vodič</a> vas vodi kroz najpoznatije
muzeje na svetu samo jednim klikom. Takođe,
posetite i izložbu „<a target="_blank" rel="nofollow" href="https://www.lapsi360.com/DCK/">Bilo jednom, ne
ponovilo se</a>“ Fonda za humanitarno pravo Kosovo. </p>

<p>Imate gitaru? Fantastično! Naučite pesme svojih omiljenih
