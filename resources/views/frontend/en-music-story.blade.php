<p><i>Emotions, feelings, memories - their first thoughts are on the word 'music'. One plays the piano and the other the guitar. One is Dragana and the other one is Rina. One creates and expresses herself artistically in Mitrovica, the other in Pristina. They do not speak the same language, but they understand each other - through music. Meet them.</i></p><p>Dragana MiliÄ‡ is a master's student at the Faculty of Arts in Mitrovica, at the Department of Music Pedagogy, Rina Neziri is studying at the Faculty of Music in Prishtina, at the Department of Guitar.</p><p><i>When did they enter the world of music?</i></p><p>"I have been surrounded by music since my childhood. That she started teaching me to play the synthesizer when I was only four years old. I enrolled in music school at the age of nine and I can say that I certainly followed in the footsteps of music," says 24-year-old Dragana. She, besides playing the piano, conducts the church choir Branislav Nusic, as deputy of the maestro Petar Rakic, composes and performs as one of the main vocals in two musical ensembles.</p><p>"I started playing music as a thirteen-year-old, when I started attending guitar classes in Ferizaj/Urosevac. Later, I enrolled in the high school of music, also in Ferizaj/Urosevac" - answers 20-year-old Rina.</p><p>Now, in their twenties, they are sovereignly stepping into the world of music, which is their refuge from the established everyday life.</p><p><i>What does music evoke in them?</i></p><p>"Every tone played or sung in each of us creates a feeling. I think that a person without emotions cannot be called a person, so in fact music makes us people as we are," says Dragana.</p><p>Rina, on the other hand, adds: "Music always brought me back to good memories, as well as bad ones. Music is the only thing that makes me move forward and get to know myself better."</p><p><i></i>Apart from music education, what makes a good musician<i></i> - we ask our interlocutors.</p><p>"I believe that every good musician must possess that X factor, a gift from God, the universe or some higher power, but what is crucial is certainly dedication, work and discipline," Dragana points out.</p><p>"To be a good musician, first of all, you have to love music, have the will and patience for everyday work and simply enjoy what music can bring you," Rina said.</p><p><i>And can good musicians and music in general contribute to mutual understanding and the connection of individuals and communities?</i></p><p>Both Dragana and Rina share the same opinion - yes!</p><p>"I believe that music can connect worlds as well! It speaks every language, even without a single word," Dragana points out.</p><p>"Music plays a big role when people need each other. Music unites us all," Rina believes.</p><p><br></p><p><br></p><div><br><br><br><br></div><p></p>
