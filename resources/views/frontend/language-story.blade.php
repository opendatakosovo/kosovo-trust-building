                               <p>Kamenica is the easternmost municipality in Kosovo. Apart from a large number of villages, beautiful landscapes, this municipality is also characterized by the fact that it is a truly multiethnic community. The message to young people from all communities is to respect each other, to work on themselves, to progress, to improve and to be friends with each other - in an interview for the Kosovo Trusbuilding platform, says the Deputy Mayor of Kamenica, Bojan StamenkoviÄ‡.</p><p>Bojan is the father of five children. He lives with his family in Kamenica. He has been the Deputy Mayor of the municipality since March 2018. In addition to Serbian, he is fluent in Albanian and English. Read below.</p><p><b>What is it like to live in Kamenica, which is considered to be one of the truly multiethnic environments and municipalities?</b></p><p>Living in Kamenica, which is a truly multiethnic environment, is a privilege.</p><p><b>On your daily job, you work with all communities in Kamenica. How would you describe your one working day?</b></p><p>As deputy mayor, I work with all communities in Kamenica, both minority and Albanian majority communities. The working day is mainly reduced to solving the requests and problems that communities have, also field work and various visits, talking to people, as well as providing all important information to citizens.</p><p><b>What hdid you learn from the having contact with people from different communities?</b></p><p>Working with people from different communities, I have learned that we are all almost the same and that we are bound by the same problems and similarities, be it mentality, tradition, etc.</p><p><b>What would you list as the biggest benefits of working in a multiethnic society?</b></p><p>The biggest advantage of working in a multiethnic society is that you have many more opportunities to get to know another community, broaden your horizons, learn, and socialize.</p><p><b>Are there any challenges you face?</b></p><p>Of course, there are challenges, no matter how much you are an example to other municipalities, there are times when you have to make decisions that do not suit everyone, and then much more attention and caution is needed for the sake of not disrupting are causing any damage to the relations between communities.</p><p><b>Is knowing your neighbor's language crucial to understanding and trusting?</b></p><p>I absolutely and responsibly claim that knowing the language of my neighbors is the most important factor in building trust and reconciliation.</p><p><b>What would be your message to young people?</b></p><p>The message to all young people from all communities is to respect each other, to work on themselves, to progress, to improve and to socialize with each other. </p><div><i>Using this text is allowed with a clear indication of the source and linking to the source of the text or the home page of the platform. &nbsp;</i><br></div><p></p>
