                            <div class="entry-blog-content">
><p>Obrazovanje je jedan od najvažnijih stubova svakog društva. Međutim, upravo se u vezi sa obrazovanjem marginalizovane zajednice često suočavaju sa različitim preprekama. Kako da im pružimo podršku? Jedan od primera dobre prakse je Roma Versitas Kosovo.</p><p>Cilj Roma Versitas Kosovo je da se poboljša zadržavanje, učinak i stepen diplomiranja romskih studenata, pružajući im akademsko podučavanje, mentorstvo i jačanje njihovog romskog identiteta i ponosa.</p><p>Model Roma Versitas takođe služi kao most za mlade romske stipendiste ka zapošljavanju i aktivnom učešću u zajednici.</p><p>RVK je 2013. godine pružio usluge za 40 učenika Roma, Aškalija i Egipćana, uspevši da poveća broj do 80 studenata za akademsku godinu.</p><p>"Ovo nije bilo dovoljno, jer se tokom godine broj studenata iz zajednica Roma, Aškalija i Egipćana povećao za preko 250 studenata na svim javnim i privatnim univerzitetima, a RV Kosovo ima sredstva za podršku za samo 80 studenata. Tokom poslednjih 8 godina RVK je pružio usluge za više od 600 studenata, a tokom ove godine 70 studenata je uspešno diplomiralo na različitim poljima studija, kao što su obrazovanje, prava, političke nauke, arhitektura, umetnost i medicina itd" – objašnjavaju iz RVK.</p><p>Pored brojeva, značaj ove vrste podrške pokazuje se u reakcijama učenika.</p><p>"Vi ste najveća podrška u mojoj karijeri. Puno vam hvala, vi ste akademija Roma Versitas Kosovo!" - samo je jedan od komentara jednog od novo diplomiranog studenta.</p><p>„Većina romski, aškajlijskijh i egipčaćnski studenata dolazi iz neobrazovanih porodica u kojima obrazovna tradicija ne postoji, takođe iz porodica koje ne stoje ekonomski i finansijski dobro, RVK je takođe u stalnom kontaktu sa donatorima koji mogu podržati studente, imamo pružamo finansijsku podršku studentima u potrebi, a nažalost ovaj broj je visok “- objašnjavaju dalje iz RVK, ističući ponovo da njihovi programi ne pružaju samo akademsku podršku:</p><p>„Takođe im nudimo inspiraciju za obrazovanje, a takođe ih pripremamo za tržište radnika kroz obuku takozvanim mekim veštinama”.</p><p>Nije iznenađenje da je poruka RVK-a romskoj, aškalijskoj i egipćanskoj omladini u pogledu obrazovanja da oni moraju da uče.</p><p>"Moraju da uče i vredno rade kako bi stvorili svetlu budućnost za sebe i svoje porodice, uz odgovarajuće obrazovanje i znanje možemo podržati sebe i svoje porodice i zajednice", zaključuju oni. </p><p>Kakva je njihova poruka široj javnosti u vezi sa ovom temom, ali I u pogledu Međunarodnog dana Roma koji je nedavno obeležen?</p><p>„U narednih 50 godina očekujemo da će većina iz romske, aškalijske i egipćanske zajednice mati svoje predstavnike, ne samo u lokalnim i centralnim institucijama, već i da imaju dobro obrazovane profesionalce koji predstavljaju Rome, Aškalije i Egipćane u parlamentu EU!“ – poruka je RVK.</p><p>Podržimo naše sugrađane u ovom cilju!</p><div><br></div><p></p>
