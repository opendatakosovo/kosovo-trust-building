                            <div class="entry-blog-content">
<p><i>Emocija, osećanja, sećanja – njihove su prve misli na reč ’muzika’. Jedna svira klavir, druga gitaru. Jedna je Dragana, a druga Rina. Jedna stvara i umetnički se izražava u Mitrovici, druga u Prištini. Ne govore istim jezikom, ali se razumeju – muzikom. Upoznajte ih.</i> </p><p>Dragana Milić je studentkinja master studija na Fakultetu umetnosti u Mitrovici, na odseku za muzičku pedagogiju, Rina Neziri studira na Fakultetu muzičke umetnosti u Prištini, na odseku za gitaru. </p><p><i>Kada su uplovile u svet muzike?</i></p><p>„Muzikom sam okružena od svog detinjstva. Deka je počeo da me podučava sviranju na sintisajzeru kada sam imala svega četiri godine. Muzičku školu upisujem sa devet godina i mogu da kažem da sam tada zasigurno krenula stopama muzike,“ kaže dvadesetčetvorogodišnja Dragana, koja osim što svira klavir, diriguje i crkvenom horu Branislav Nušic, kao zamenik maestra Petra Rakića, komponuje i nastupa kao jedan od  glavnih vokala u dva muzička sastava.</p><p>„Muzikom sam počela da se bavim kao trinaestogodišnjakinja, kada sam počela da pohađam časove gitare u Uroševcu. Kasnije upisujem i srednju muzičku školu, takođe u Uroševcu,“ odgovara dvadesetogodišnja Rina.</p><p>Sada, u svojim dvadesetim, suvereno koračaju svetom muzike, koji im je i utočište od ustaljene svakodnevnice. <i>Šta muzika budi u njima?</i></p><p>„Svaki odsvirani ili otpevani ton u svakome od nas stvara neki osecaj. Smatram da se čovek bez emocija i ne može nazvati čovekom, pa nas zapravo muzika čini ljudima kakvi jesmo,“ govori Dragana.</p><p>Rina, sa druge strane, dodaje:</p><p>„Muzika me je uvek vraćala u dobre uspomene, kao i u one loše. Muzika je jedina stvar koja me tera da idem napred i da bolje upoznam sebe.“</p><p>Osim muzičkog obrazovanja, šta čini dobrog muzičara, pitamo naše sagovornice.</p><p>„Smatram da svaki dobar muzičar mora da poseduje onaj X faktor, dar od Boga, univerzuma ili neke više sile, ali ono što je presudno je svakako posvećenost, rad i disciplina,“ ističe Dragana.</p><p>„Da biste bili dobar muzičar, pre svega, morate voleti muziku, imati volju i strpljenje za svakodnevni rad i jednostavno uživati u onom što vam muzika može doneti,“ stav je Rine.</p><p><i>A mogu li dobri muzičari i muzika doprineti međusobnom razumevanju i spajanju pojedinaca i zajednica?</i></p><p>I Dragana i Rina dele isto mišljenje – da!</p><p>„Verujem da muzika može da spaja i svetove! Ona govori na svakom jeziku, čak i bez ijedne reči,“ ističe Dragana.</p><p>„Muzika igra veliku ulogu kada su ljudi potrebni jedni drugima. Muzika nas sve ujedinjuje,“ veruje Rina.</p><p>Naše mlade sagovornice pripadaju mlađim generacijama koje stvaraju na Kosovu i koje bi trebalo da ruše mostove i doprinesu izgradnji poverenja. </p><p>Šta je za njih poverenje?</p><p>„Bezuslovna ljubav bez koje nijedan odnos među ljudima ne može da uspe,“ navodi Dragana.</p><p>„Poverenje je za mene osećaj ljubavi, stabilnosti, brige za nekoga ili nešto,“ zaključuje Rina.</p><p><br></p><p><br></p>
